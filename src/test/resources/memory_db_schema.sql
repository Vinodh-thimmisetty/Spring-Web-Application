SET DATABASE SQL SYNTAX ORA TRUE;
CREATE SCHEMA VINODH
	CREATE TABLE COUNTRIES_LIST (
		COUNTRY_ID		INT NOT NULL,
		COUNTRY_NAME	VARCHAR(100) NOT NULL,
		COUNTRY_CODE	VARCHAR(100) NOT NULL
	)
	
	CREATE TABLE INDIAN_STATES (
 		STATE_ID INT,
   		STATE_NAME VARCHAR2(50) NOT NULL
    )
    
    CREATE TABLE EMPLOYEE (
 		EMP_ID INT,
   		EMP_NAME VARCHAR2(50) NOT NULL,
   		EMP_PHONE INT,
   		EMP_SALARY INT,
   		EMP_EMAIL VARCHAR2(50) NOT NULL,
   		EMP_DOB DATE
    )
    
    CREATE TABLE USER_INFORMATION (
		  ID INT,	/**  GENERATED BY DEFAULT AS IDENTITY(START WITH 100, INCREMENT BY 1) PRIMARY KEY **/
		  USER_COUNTRY          VARCHAR2(255),
		  FIRST_NAME            VARCHAR2(255),
		  USER_GENDER           VARCHAR2(255),
		  LAST_NAME             VARCHAR2(255),
		  USER_PHONE            VARCHAR2(255),
		  USER_STATE            VARCHAR2(255),
		  USER_EMAIL            VARCHAR2(255),
		  USER_NAME             VARCHAR2(255),
		  USER_PASSWORD         VARCHAR2(255),
		  EMAIL_VALIDATION_KEY  VARCHAR2(255),
		  TOKEN_GENERATED_TIME  DATE
	)
	
	CREATE TABLE ADMIN_INFORMATION
	(
	  ADMIN_ID                  NUMBER(10)          NOT NULL,
	  ADMIN_LOGIN_NAME          VARCHAR2(255 CHAR),
	  ADMIN_EMAIL               VARCHAR2(255 CHAR),
	  ADMIN_FIRST_NAME          VARCHAR2(255 CHAR),
	  ADMIN_LAST_NAME           VARCHAR2(255 CHAR),
	  ADMIN_PASSWORD            VARCHAR2(255 CHAR),
	  ADMIN_PHONE               NUMBER(19),
	  HOMEADDRESS_ADDRESS_ID    NUMBER(10),
	  OFFICEADDRESS_ADDRESS_ID  NUMBER(10)
	)
	
	CREATE TABLE ADMIN_ADDRESS_INFORMATION
	(
	  ADDRESS_ID     NUMBER(10)                     NOT NULL,
	  CITY_NAME      VARCHAR2(255 CHAR),
	  CONTINENT      VARCHAR2(255 CHAR),
	  COUNTRY_NAME   VARCHAR2(255 CHAR),
	  DISTRICT_NAME  VARCHAR2(255 CHAR),
	  DOOR_NUMBER    VARCHAR2(255 CHAR),
	  STATE_NAME     VARCHAR2(255 CHAR),
	  STREET_NAME    VARCHAR2(255 CHAR)
	)
 CREATE TABLE VINODH.COURSE_INFORMATION
	(
	  COURSE_ID                       NUMBER(10)    NOT NULL,
	  COURSE_DESCRIPTION              VARCHAR2(255 CHAR),
	  COURSE_NAME                     VARCHAR2(255 CHAR),
	  COURSE_PRICE                    NUMBER(19),
	  COURSE_START_DATE               DATE,
	  COURSE_TECHNOLOGY               VARCHAR2(255 CHAR),
	  COURSEINSTRUCTOR_INSTRUCTOR_ID  NUMBER(10)
	)
	 
	CREATE TABLE INSTRUCTOR_INFORMATION
	(
	  INSTRUCTOR_ID     NUMBER(10)                  NOT NULL,
	  INSTRUCTOR_EMAIL  VARCHAR2(255 CHAR),
	  INSTRUCTOR_EXP    NUMBER(10),
	  INSTRUCTOR_NAME   VARCHAR2(255 CHAR),
	  INSTRUCTOR_TECH   VARCHAR2(255 CHAR)
	)
	
	CREATE TABLE AUTHENTICATION_USER_DETAILS
	(
		USER_ID	NUMBER,
		USER_FIRST_NAME	VARCHAR2(100 BYTE) NOT NULL,
		USER_LAST_NAME	VARCHAR2(100),
		USER_EMAIL	VARCHAR2(64)  NOT NULL,
		USER_PHONE	NUMBER,
		USER_ROLE	VARCHAR2(20) NOT NULL,
		APPLICATION_NAME VARCHAR2(100 BYTE) NOT NULL,
		LAST_MODIFIED_BY	VARCHAR2(100),
		LAST_MODIFIED_DATE	DATE
	)
 
	GRANT SELECT,UPDATE,INSERT,DELETE ON COUNTRIES_LIST TO PUBLIC
	GRANT SELECT,UPDATE,INSERT,DELETE ON INDIAN_STATES TO PUBLIC
	GRANT SELECT,UPDATE,INSERT,DELETE ON EMPLOYEE TO PUBLIC
	GRANT SELECT,UPDATE,INSERT,DELETE ON ADMIN_INFORMATION TO PUBLIC
	GRANT SELECT,UPDATE,INSERT,DELETE ON ADMIN_ADDRESS_INFORMATION TO PUBLIC
	GRANT SELECT,UPDATE,INSERT,DELETE ON COURSE_INFORMATION TO PUBLIC
	GRANT SELECT,UPDATE,INSERT,DELETE ON INSTRUCTOR_INFORMATION TO PUBLIC
	GRANT SELECT,UPDATE,INSERT,DELETE ON AUTHENTICATION_USER_DETAILS TO PUBLIC;